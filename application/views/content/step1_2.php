        function submit_teamManagementForm(){

            var interests       = "";

            var interestsChecked = $("input:checkbox[name='interests[]']:checked");
            var len = interestsChecked.length;

            interestsChecked.each(function(index, element){
                interests += $(this).val() + ((index==len-1)? "":", ");
            });

            var formData ={
                ajax        :       '1',
                name        :       $('#txtname').val(),
                title       :       $('#txttitle').val(),
                organization:       $('#txtorganization').val(),
                email       :       $('#txtemail').val(),
                phone       :       $('#txtphone').val(),
                interest    :       interests

            };


            $.ajax({
                url: '<?php echo(base_url('team/add')); ?>',
                data: formData,
                type:'POST',
                success:function(response){
                    //alert(response);
                    var res = JSON.parse(response);
                    if(res.saved==true){

                        clearFormInputFields(); // Clear form data from the fields
                        /*$('#teamManagementForm').hide('slow');*/
                        loadTeams();
                    }
                    else{
                        alert("Error creating New Team Member!");
                    }
                },
                error:function(error){
                    alert(error);
                }
            });

            return false;

        }

        $("#teamManagementForm").validate({
            rules: {
                txtphone:{
                    phoneUS2: true
                }
            },
            submitHandler: submit_teamManagementForm
        });



        function loadTeams(){

            var formData = {
                ajax    :   '1',
                param   :   'all'
            };

            $.ajax({
                url: '<?php echo(base_url('team/show')); ?>',
                data: formData,
                type:'POST',
                success:function(response){
                    $('#subDetailDiv').html(response);

                },
                error:function(error){
                    //alert(error);
                }
            });
        }

